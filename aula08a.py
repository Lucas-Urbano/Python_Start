from math import sqrt #Importa funcionalidade específica da biblioteca#
num = int(input("Digite um número: "))
raiz = sqrt(num)
print(" A raiz de {} é igual {} ".format(num, raiz))


import math #Importando toda biblioteca#
num2 = int(input("Digite outro número: "))
raiz2= math.sqrt(num2)
print("A raiz de {} é igual {:.2f} ".format(num2, raiz2))

